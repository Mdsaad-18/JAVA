public class SumOfArray { 
    public static void main(String args[]){
		 
		 int numbers[] = {11,22,33,44,55,66,77,88,99};
		 
		 int sum = 0;
		 
		 for (int i=0; i<numbers.length; i++){
			 sum = sum + numbers[i];
			 
			
		 }
		 System.out.println("the sum of all the elements of arrays are:  "+ sum);
	}
}	
   