  public class Array2 {
      public static void main(String args[]){
	    // by taking the example of fruits
		
		String Fruitnames[] = {"Apple","Banana","Grapes","Watermilon","Mango"};
		int Fruitrate[] = {100,200,300,400,500};
		long FruitOwnerNum[] = {990210,636408,147852,369852,123654};
		char FruitQuality[] = {'A','B','C','D','E'};
		
		for (int b=0; b<Fruitnames.length; b++){
			System.out.println("Fruit names   "  +Fruitnames[b]);
			System.out.println("Fruit Fruitrate   "  +Fruitrate[b]);
			System.out.println("Fruit FruitOwnerNum is   "  +FruitOwnerNum[b]);
			System.out.println("Fruit FruitQuality  "  +FruitQuality[b]);
			
			System.out.println("\n");
		}
	  }
  }
			
		
		
		
		